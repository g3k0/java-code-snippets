package conditionals;

public class ApplicationIf {
    public static void main(String[] args) {
        int myInt = 20;
        if (myInt < 30) {
            System.out.println("Yes, it's true");
        } else if (myInt < 25) {
            System.out.println("Another condition");
        } else {
            System.out.println("No, it's false");
        }
     }
}
